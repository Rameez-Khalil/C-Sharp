/*
 * ENCAPUSLATION: 
 *      Encapsulation is about packaging the data inside the container, and making functions available
 *      which have the authory to manipulate the data.
 *      
 *      This can be achieved by making variables private, and exposing getters and setters.
 *      
 *      
 *      private string name; 
 *      private int id; 
 *      
 *      public string Name{get; set;]
 *      public int id {get; set;}.
 *      
 */