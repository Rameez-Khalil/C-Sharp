/*
 * METHOD OVERRIDING: 
 *      This is another way to achieve polymorphism, this is a technique through which we make our methods signatures
 *      the same, on the other hand, we manipulate their behaviors.
 *      
 *      Virtual denotes the method is getting overriden.
 *      Override denots the method is overriding.
 *      
 *      Class Animal{
 *          public string name;
 *          public virtual void makeSound(){
 *              //Code
 *              }
 *     class Dog : Animal{
 *          public override override makeSound(){
 *                  //Changed code.
 *          }
 */